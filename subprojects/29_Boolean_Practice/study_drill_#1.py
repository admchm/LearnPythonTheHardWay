## Study Drill #1: 
## There are a lot of operators in Python similar to != and ==. Try to 
## find as many "equality operators" as you can. They should be like < or <=.

# Answer: 
# == (Equal to)
# != (Not equal to)
# > (Greater than)
# < (Less than)
# >= (Greater than or equal to)
# <= (Less than or equal to)